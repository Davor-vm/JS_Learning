let a = 10;
console.log(a > 5); // true
console.log(a >= 5); // true
console.log(a < 5); // false
console.log(a <= 5); // false

console.log (a == 10); // true
console.log (a != 10); // false
console.log (a == '10'); // true (comparación de valor)
console.log (a === 10); // true (comparación de valor y tipo)
console.log (a !== 10); // false (comparación de valor y tipo)
